												2. Solid Waste Management: Ecobins’; separate waste bins (made from card boards) for
												paper/biodegradable waste and plastic and non biodegradable waste are installed in all our class
												rooms, staffroom and labs. Ecobins made of cane and bamboo are provided in every common place and
												corridors. The bins are collected by sweepers everyday and segregated into solid waste, paper, other
												degradable waste and plastic at the segregation room. This is again given for recycling to the local
												scrap collectors as instructed by the district level Suchithwa mission. Recycling of waste paper is
												also carried out by our department. All the waste paper is being used for printing notices, internal
												question papers and reading materials so that use of paper is minimised for official purpose. After
												using both the sides, the papers are sent for recycling.
