								<h6>54) Uniqueness of the department</h6>

								<div class="table-responsive">
									<table class="table table-bordered">
										<tr>
											<td style="background-color: #d9d9d9" colspan="8">
												Brief description of the Uniqueness of the department
											</td>
										</tr>
										<tr>
											<td style="height: 35%" colspan="8">
												The Research and PG department of BOTANY is full fledged with 6 permanent faculty members. The
												faculties of the Department have ample expertise in different fields of botany especially Algal
												study, Forest ecology and Taxonomy, Stress Physiology and Environment and Conservation. These
												expertise has brought significant results influencing Research in this field and policy and action
												in the State. Some of the pioneering work in Conservation and policy inputs evolved from these
												expertise has become a model for the state which has to be adopted to the system. This expertise,
												research and social intervention platform has become unique to the department which attract students
												from various districts of the state. This research facilities has been made useful for the
												upliftment of the UG and PG students in the college. The uniqueness of the department reflected in
												important activities of the college as a whole.<br />
												All faculties keep a warm relationship among themselves and also with the students of the department
												as TEAM BOTANY. The team always works with a motive for a green and clean environment. Students are
												encouraged and trained in gardening, vegetable cultivation, making eco-friendly products and waste
												management. A Student Aid Fund is functioning in the department for helping the financially weak
												students. BHOOMITHRASENA club is functioning in the department. As an attempt to protect our
												traditional food habits, ‘karkkidakamasacharanam’ is conducted in the department every year.
											</td>
										</tr>
